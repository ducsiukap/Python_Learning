# syntax : variableName[:data_type] = value

#   INTEGER
iNumber1 = 3
iNumber2:int = -5
print(f'type of iNumber1: {type(iNumber1)} \nvalue of iNumber1: {iNumber1}')
print(f'type of iNumber2: {type(iNumber2)} \nvalue of iNumber1: {iNumber2}')
print()

#   FLOAT
fNumber = 9.9
print(f'type of fNumber: {type(fNumber)} \nvalue of fNumber: {fNumber}')
print()

#   COMPLEX
cNumber = 3 + 2j
print(f'type of cNumber: {type(cNumber)} \nvalue of cNumber: {cNumber}')
print(f'type of cNumber.real: {type(cNumber.real)} \ntype of cNumber.imag: {type(cNumber.imag)}')
print()
